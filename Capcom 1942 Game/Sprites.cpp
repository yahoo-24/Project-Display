void UI::draw_number(int number, int y) {
    // Determines what the number is and draws its equivalent sprite.
    switch (number) {
        case 0:
            lcd.drawSprite(80, y, 5, 3, (int *)Zero);
            break;
        case 1:
            lcd.drawSprite(80, y, 5, 3, (int *)One);
            break;
        case 2:
            lcd.drawSprite(80, y, 5, 3, (int *)Two);
            break;
        case 3:
            lcd.drawSprite(80, y, 5, 3, (int *)Three);
            break;
        case 4:
            lcd.drawSprite(80, y, 5, 3, (int *)Four);
            break;
        case 5:
            lcd.drawSprite(80, y, 5, 3, (int *)Five);
            break;
        case 6:
            lcd.drawSprite(80, y, 5, 3, (int *)Six);
            break;
        case 7:
            lcd.drawSprite(80, y, 5, 3, (int *)Seven);
            break;
        case 8:
            lcd.drawSprite(80, y, 5, 3, (int *)Eight);
            break;
        case 9:
            lcd.drawSprite(80, y, 5, 3, (int *)Nine);
            break;
    }
}

void UI::draw_lives(int lives) {
    // Draws a 3x2 square for each life (max. 3)
    if (lives > 0) {
        lcd.drawRect(80, 46, 3, 2, FILL_BLACK);
    } 
    if (lives > 1) {
        lcd.drawRect(80, 43, 3, 2, FILL_BLACK);
    }
    if (lives > 2) {
        lcd.drawRect(80, 40, 3, 2, FILL_BLACK);
    }
}

void UI::draw_score(int score) {
    score = score % 1000; // Keep the score at 3 digits to display
    draw_number(score / 100, 0); // Display the Hundreds
    draw_number((score % 100) / 10, 6); // Display the Tens
    draw_number((score % 100) % 10, 12); // Display the Zeros
}

void UI::draw_hitpoints(int hitpoints, int max_hp) {
    // A function to draw the hitpoints bar. 20 bars in total
    lcd.drawRect(80, 18, 3, ((float) hitpoints / max_hp) * 20, FILL_BLACK);
}

void UI::separate() {
    // Draws a line to separate the UI from the game
    lcd.drawLine(79, 0, 79, 0, 2);
}


